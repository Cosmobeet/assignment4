// ADD FUNCTION
const add = (a, b) => {
  return a + b;
};
// SUBTRACT FUNCTION
const sub = (a, b) => {
  return a - b;
};
// MULTIPLY FUNCTION
const mult = (a, b) => {
  return a * b;
};
// DIVIDE FUNCTION
const div = (a, b) => {
  return a / b;
};
// EXPORT THE FOUR PUBLIC FUNCTIONS
export { add, sub, mult, div };
